HANZI_WRITER_CDN = "https://cdn.jsdelivr.net/npm/hanzi-writer-data@2.0/{char}.json"

class StrokeDataManager:
    def __init__(self, use_online=True, cache_dir=None):
        self.local_db = LOCAL_STROKE_DATABASE
        self.use_online = use_online
        self.cache_dir = cache_dir
        
        if self.cache_dir and not os.path.exists(self.cache_dir):
            os.makedirs(self.cache_dir)

    def get_stroke_order(self, char):
        if char in self.local_db:
            return self.local_db[char]
        
        if self.use_online and HAS_URLLIB:
            online_strokes = self._get_from_online(char)
            if online_strokes:
                self.local_db[char] = online_strokes
                return online_strokes
        
        return None

    def _get_from_online(self, char):
        try:
            url = HANZI_WRITER_CDN.format(char=char)
            
            if self.cache_dir:
                cache_file = os.path.join(self.cache_dir, f"{ord(char)}.json")
                if os.path.exists(cache_file):
                    try:
                        with open(cache_file, 'r', encoding='utf-8') as f:
                            data = json.load(f)
                        return self._parse_stroke_data(data)
                    except:
                        pass
            
            req = urllib.request.Request(
                url,
                headers={'User-Agent': 'Mozilla/5.0'}
            )
            with urllib.request.urlopen(req, timeout=5) as response:
                data = json.loads(response.read().decode('utf-8'))
            
            if self.cache_dir:
                cache_file = os.path.join(self.cache_dir, f"{ord(char)}.json")
                try:
                    with open(cache_file, 'w', encoding='utf-8') as f:
                        json.dump(data, f, ensure_ascii=False, indent=2)
                except:
                    pass
            
            return self._parse_stroke_data(data)
            
        except Exception as e:
            return None

    def _parse_stroke_data(self, data):
        if 'strokes' not in data:
            return None
        
        strokes = data['strokes']
        stroke_names = []
        
        for stroke in strokes:
            name = self._identify_stroke(stroke)
            stroke_names.append(name)
        
        return stroke_names if stroke_names else None

    def _identify_stroke(self, svg_path):
        if not svg_path:
            return '横'
        
        path_lower = svg_path.lower()
        
        if 'q' in path_lower or 'c' in path_lower:
            if 'v' in path_lower:
                if 'h' in path_lower:
                    return '横折钩'
                return '竖弯钩'
            if 'h' in path_lower:
                return '横钩'
            return '撇'
        
        if 'a' in path_lower:
            return '斜钩'
        
        coords = self._extract_coords(svg_path)
        if len(coords) < 2:
            return '横'
        
        x1, y1 = coords[0]
        x2, y2 = coords[-1]
        
        dx = abs(x2 - x1)
        dy = abs(y2 - y1)
        
        if dx > dy * 2:
            return '横'
        
        if dy > dx * 2:
            if y2 > y1:
                return '竖'
            else:
                return '竖'
        
        if x2 > x1 and y2 > y1:
            return '捺'
        
        if x2 < x1 and y2 > y1:
            return '撇'
        
        return '横'

    def _extract_coords(self, svg_path):
        coords = []
        try:
            parts = re.split(r'[MLCQCAZ\s,]+', svg_path.strip(), flags=re.IGNORECASE)
            nums = [float(p) for p in parts if p]
            
            for i in range(0, len(nums) - 1, 2):
                coords.append((nums[i], nums[i + 1]))
        except:
            pass
        
        return coords

    def add_to_local_database(self, char, strokes):
        if len(strokes) > 0 and isinstance(strokes[0], str):
            self.local_db[char] = strokes
            return True
        return False

    def get_available_chars_count(self):
        return len(self.local_db)

    def get_all_available_chars(self):
        return sorted(self.local_db.keys())

